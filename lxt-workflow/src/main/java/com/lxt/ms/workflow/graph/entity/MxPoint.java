package com.lxt.ms.workflow.graph.entity;

public class MxPoint {
	private Float x;
	private Float y;
	private String as;

	public Float getX() {
		return x;
	}

	public void setX(Float x) {
		this.x = x;
	}

	public Float getY() {
		return y;
	}

	public void setY(Float y) {
		this.y = y;
	}

	public String getAs() {
		return as;
	}
	
	public void setAs(String as) {
		this.as = as;
	}
}
